    function enviarCorreo($from, $nombre, $mensaje){
        // Create the transport
        $transport = Swift_SmtpTransport::newInstance('smtp.gmail.com', 587, 'tls')
            ->setUsername('gilbertgol@gmail.com')
            ->setPassword('g1lb3rt0')
        ;

        // Create the mailer
        $mailer = Swift_Mailer::newInstance($transport);
        
        // Create the message
        $message = Swift_Message::newInstance()

            // Give the message a subject
            ->setSubject('Email de contacto de cv.gilbertoorozco.net')

            // Set the From address with an associative array
            ->setFrom(array($from => $nombre))

            // Set the To addresses with an associative array
            ->setTo(array('gilbertgol@gmail.com'))

            // Give it a body
            ->setBody("<b>Mensaje desde </b>cv.gilbertoorozco.net<br><b>Enviado por: </b>" . $nombre . " - " . $from . "<br><br>" . $mensaje, 'text/html')
        ;
        
        $result = $mailer->send($message);

        return $result;
    }
